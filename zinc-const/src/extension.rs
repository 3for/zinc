//!
//! The Zinc file extensions.
//!

/// The Zinc manifest file extension.
pub static MANIFEST: &str = "toml";

/// The Zinc source code file extension.
pub static SOURCE: &str = "zn";

/// The Zinc binary file extension.
pub static BINARY: &str = "znb";

/// The Zinc template file extension.
pub static TEMPLATE: &str = "json";

/// The Zinc verifying_key file extension.
pub static VERIFYING_KEY: &str = "txt";
